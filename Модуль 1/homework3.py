my_string = input('Введите текст: ')
print(len(my_string))  # длина строки

print(my_string.upper())  # my_string в верхнем регистре
print(my_string.lower())  # my_string в нижнем регистре
print(my_string.replace(' ', ''))  # my_string без пробелов
print(my_string[0])  # первый символ строки my_string
print(my_string[-1])  # последний символ строки my_string
