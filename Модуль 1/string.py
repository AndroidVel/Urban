name = 'это моя строка'
print(name[0])  # первый символ
print(name[-1])  # последний символ
print(name[3:6])  # подстрока с третьего по пятый символы
print(name[::-1])  # строка наоборот
print(len(name))  # длина строки
print('это новая стока' + name)  # соединение строки 'это новая строка' и 'это моя строка'
